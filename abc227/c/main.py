N = int(input())
ans = 0
a = 1
while a*a*a <= N:
    b = a
    while a*b*b <= N:
        # コンテスト中はA, Bが決まったらCを二分探索で求めてしまったが、CはO(1)で求められた。
        # 振り返ると、具体例で確かめればよかった。
        # 例えば、
        # N=100
        # A=2
        # B=5
        # のとき、Cは最大10（問題の制約から）最小5の6個だとわかり、解けたかもしれない。
        ans += N//a//b-b+1
        b += 1
    a += 1
print(ans)
